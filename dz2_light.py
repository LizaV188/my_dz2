'''Задача 4 Найти произведение ряда чисел от 1 до 10. Полученный результат вывести на экран(можно поискать в 
интернете алгоритм факториала в python). 
'''
#уже решала эту задачу в курсе по нейронкам, используется алгоритм дерева:

def ProdTree(l, r):
  if (l > r):
    return 1
  if (l == r):
    return l
  if (r - l == 1):
    return l*r

  m = int((l+r)/2)
  return ProdTree(l,m)*ProdTree(m+1,r)

def FactTree(N):
  if N<0 or type(N) == float:
    return ('Ошибка. Факториал определен на множестве неотрицательных целых чисел!')
  if N == 0:
    return 1
  if N == (1 or 2):
    return N
  return ProdTree(2,N)

